// agix-eval/report — markdown renderer for suite + fleet results.
// House style matches scripts/stv-eval.mjs: a verdict line, a gate
// table, then per-suite detail. Every reported aggregate that has one
// carries its 95% CI.

const pct = (x) => (Number.isFinite(x) ? `${(x * 100).toFixed(1)}%` : 'n/a');
const f3 = (x) => (Number.isFinite(x) ? x.toFixed(3) : 'n/a');
const mark = (b) => (b ? '✅' : '❌');

function renderGateTable(gates) {
  const rows = Object.entries(gates).map(([key, g]) => {
    const bar = `${g.op} ${typeof g.bar === 'number' ? f3(g.bar) : g.bar}`;
    const val = typeof g.value === 'number' ? f3(g.value) : String(g.value);
    return `| ${g.label} | ${val} | ${bar} | ${mark(g.passed)} |`;
  });
  return `| Gate | Value | Bar | Pass |\n|---|---|---|---|\n${rows.join('\n')}`;
}

function renderConfusion(report) {
  const cols = [...report.classes, '_none_'];
  const head = `| gold \\ pred | ${cols.join(' | ')} |`;
  const sep = `|${'---|'.repeat(cols.length + 1)}`;
  const body = [...report.classes, '_none_']
    .map((g) => `| **${g}** | ${cols.map((p) => report.confusion[g][p]).join(' | ')} |`)
    .join('\n');
  return `${head}\n${sep}\n${body}`;
}

export function renderSuite(r) {
  const lines = [];
  lines.push(`### ${r.agent} — ${r.name}  ${mark(r.allGatesPassed)}`);
  lines.push('');
  lines.push(`> Archetype: **${r.archetype}** · mode: **${r.mode}** · cases: **${r.n}**`);
  lines.push(`> ${r.description}`);
  lines.push('');
  lines.push(renderGateTable(r.gates));
  lines.push('');

  const m = r.metrics || {};
  if (m.classification) {
    const cr = m.classification;
    lines.push(`**Classification** — accuracy ${pct(cr.accuracy)} · macro-F1 ${f3(cr.macroF1)} (macro-P ${f3(cr.macroPrecision)} / macro-R ${f3(cr.macroRecall)}) over ${cr.total} labels.`);
    lines.push('');
    lines.push('Per-class F1:');
    lines.push('');
    lines.push('| class | precision | recall | F1 | support |');
    lines.push('|---|---|---|---|---|');
    for (const c of cr.classes) {
      const pc = cr.perClass[c];
      lines.push(`| ${c} | ${f3(pc.precision)} | ${f3(pc.recall)} | ${f3(pc.f1)} | ${pc.support} |`);
    }
    lines.push('');
    lines.push('Confusion matrix:');
    lines.push('');
    lines.push(renderConfusion(cr));
    lines.push('');
  }
  if (m.findings) {
    const fd = m.findings;
    lines.push(`**Critic findings vs gold** — precision ${f3(fd.precision)} · recall ${f3(fd.recall)} · F1 ${f3(fd.f1)} (tp=${fd.tp} fp=${fd.fp} fn=${fd.fn}).`);
    lines.push('');
  }
  if (m.score) {
    const s = m.score;
    lines.push(`**Mean score** ${f3(s.mean)} · 95% CI [${f3(s.ci95[0])}, ${f3(s.ci95[1])}] (n=${s.n}, SEM ${f3(s.sem)}).`);
    lines.push('');
  }
  if (m.passRate) {
    const p = m.passRate;
    lines.push(`**Case pass rate** ${pct(p.p)} · Wilson 95% CI [${pct(p.ci95[0])}, ${pct(p.ci95[1])}] (n=${p.n}).`);
    lines.push('');
  }
  if (m.guardrails) {
    lines.push(`**Guardrails** ${m.guardrails.passed}/${m.guardrails.total} held (${pct(m.guardrails.rate)}).`);
    lines.push('');
  }
  if (m.safeAction) {
    const sa = m.safeAction;
    lines.push(`**Safe-action gate** — blocked ${sa.blocked}/${sa.unsafeTotal} unsafe actions (${pct(sa.blockedUnsafeRate)}) · over-blocked (false denials) ${sa.overBlocked}.`);
    lines.push('');
  }
  if (m.mast) {
    lines.push('**MAST failure-mode coverage** (orchestrator):');
    lines.push('');
    lines.push('| category | cases | correct |');
    lines.push('|---|---|---|');
    for (const cat of Object.keys(m.mast)) {
      lines.push(`| ${cat} | ${m.mast[cat].total} | ${m.mast[cat].correct} |`);
    }
    lines.push('');
  }
  if (m.reliability) {
    const rel = m.reliability;
    lines.push(`**Reliability** — pass@1 ${pct(rel.passAt1)} · pass^${rel.k} ${pct(rel.passPowK)} (capability vs all-of-${rel.k} reliability).`);
    lines.push('');
  }

  // Per-case failures (the actionable bit).
  const fails = r.perCase.filter((c) => c.passed === false);
  if (fails.length) {
    lines.push(`<details><summary>${fails.length} failing / flagged case(s)</summary>`);
    lines.push('');
    for (const c of fails) {
      lines.push(`- \`${c.label}\` — score ${f3(c.score)}${c.detail ? ` — ${c.detail}` : ''}${c.error ? ` — ERROR: ${c.error}` : ''}`);
    }
    lines.push('');
    lines.push('</details>');
    lines.push('');
  }
  return lines.join('\n');
}

export function renderReport(fleet, { title = 'Agix Agents — Evaluation Report', generatedBy = 'scripts/agix-eval.mjs' } = {}) {
  const date = new Date().toISOString().slice(0, 10);
  const head = [];
  head.push(`# ${title}`);
  head.push('');
  head.push(`> Generated by \`${generatedBy}\` on ${date} · mode: **${fleet.mode}**.`);
  head.push(`> Methodology + citations: \`wiki/research/2026-06-05-agent-evaluation-methodology.md\`.`);
  head.push('');
  head.push(`**Verdict: ${fleet.allPassed ? 'ALL SUITES PASSED ✅' : 'GATE FAILURE ❌'}** — ${fleet.suitesPassed}/${fleet.suitesTotal} suites green.`);
  head.push('');
  head.push('| Suite | Agent | Archetype | Cases | Mode | Result |');
  head.push('|---|---|---|---|---|---|');
  for (const s of fleet.suites) {
    head.push(`| ${s.name} | ${s.agent} | ${s.archetype} | ${s.n} | ${s.mode} | ${mark(s.allGatesPassed)} |`);
  }
  head.push('');
  head.push('---');
  head.push('');
  const body = fleet.suites.map(renderSuite).join('\n---\n\n');
  return `${head.join('\n')}\n${body}\n`;
}

export { pct, f3, mark };
